#include <iostream>
using namespace std;

int main() {
    int num1, num2, sum;

    // Input two numbers
    cout << "Enter first number: ";
    cin >> num1;

    cout << "Enter second number: ";
    cin >> num2;

    // Add the numbers
    sum = num1 + num2;

    // Display the result
    cout << "Sum of " << num1 << " and " << num2 << " is: " << sum << endl;

    return 0;
}
